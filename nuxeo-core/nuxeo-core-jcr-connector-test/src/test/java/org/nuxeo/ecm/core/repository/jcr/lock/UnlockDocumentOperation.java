package org.nuxeo.ecm.core.repository.jcr.lock;

import org.nuxeo.ecm.core.api.ClientException;
import org.nuxeo.ecm.core.api.CoreSession;
import org.nuxeo.ecm.core.api.DocumentModel;

public class UnlockDocumentOperation extends AbstractRepositoryOperation {

    public UnlockDocumentOperation(CoreSession session, DocumentModel doc) {
        super(session, doc);
    }
    
    protected void doOperate() throws ClientException {
        if (session.getLock(doc.getRef()) != null)
            return;
        session.unlock(doc.getRef());
    }
}